class Solution {
public:
	int search(vector<int>& nums, int target) {
		if (nums.empty()) return -1;

		// 二分出旋转的分界点
		int l = 0, r = nums.size() - 1;
		while (l < r) {
			int mid = l + r + 1 >> 1;
			if (nums[mid] >= nums[0]) l = mid;
			else r = mid - 1;
		}

		// 确定target在的区间，二分查找
		if (target >= nums[0]) l = 0;
		else l = r + 1, r = nums.size() - 1;
		while (l < r) {
			int mid = l + r >> 1;
			if (nums[mid] >= target) r = mid;
			else l = mid + 1;
		}
		if (target == nums[r]) return r;
		else return -1;
	}
};